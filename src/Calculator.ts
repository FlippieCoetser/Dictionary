/** 
 * @module Calculator 
 * */

export class Calculator {
    static Add = (a: number, b: number): number => a + b;
    static Subtract = (a: number, b: number): number => a - b;
    static Multiply = (a: number, b: number): number => a * b;
    static Divide = (a: number, b: number): number => a / b;
}